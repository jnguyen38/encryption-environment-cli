void SHA256::revert(unsigned char * hash) {
	// SHA uses big endian byte ordering
	// Revert all bytes
	for (unsigned char i = 0 ; i < 4 ; i++) {
		for(unsigned char j = 0 ; j < 8 ; j++) {
			hash[i + (j * 4)] = (m_state[j] >> (24 - i * 8)) & 0x000000ff;
		}
	}
}

